public class VarsAndMath {
    public static void main(String[] args) {

        int a = 10;
        int b = 3;

        System.out.println("a = " + a + ", b = " + b);
        System.out.println("a + b = " + (a + b));
        System.out.println("a * b = " + (a * b));
    }
}